namespace DW.WPFToolkit.Controls
{
    /// <summary>
    /// Defines which kind of numbers the <see cref="DW.WPFToolkit.Controls.NumberBox" /> is accepting.
    /// </summary>
    public enum NumberTypes
    {
        /// <summary>
        /// The <see cref="DW.WPFToolkit.Controls.NumberBox" /> is accepting integer values only. This includes negative values.
        /// </summary>
        Integer,

        /// <summary>
        /// The <see cref="DW.WPFToolkit.Controls.NumberBox" /> is accepting double values. This includes negative values.
        /// </summary>
        Double
    }
}
